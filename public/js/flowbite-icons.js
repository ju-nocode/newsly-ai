/**
 * Get an icon by name
 * @param {string} name - Icon name
 * @param {string} className - Optional custom classes
 * @returns {string} SVG HTML string
 */
export function getIcon(name, className) {
    if (!icons[name]) {
        console.warn(`Icon "${name}" not found in flowbite-icons`);
        return '';
    }

    if (className) {
        // Override default classes if provided
        return icons[name]().replace(/class="[^"]*"/, `class="${className}"`);
    }

    return icons[name]();
}
